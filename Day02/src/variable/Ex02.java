package variable;

// ctrl + space : 이클립스 자동완성

public class Ex02 {
	public static void main(String[] args) {
		// 자료형 : 데이터 타입. 코드 내에서 데이터를 식별하는 체계
		
		// 문자열 	: " " 로 이루어진 데이터, 단어나 문장을 표현		ex) "10"
		// 정수	: " " 가 없는 숫자, 연산을 위한 데이터			ex) 10
		// 실수	: " " 가 없는 숫자 중 .(소수점)이 포함된 데이터		ex) 10.0
		
		
		System.out.println("10" + "2");		// 문자열의 + 연산은 이어 붙는다
		System.out.println(10 + 2);			// 정수는 실제 연산이 이루어진다
		System.out.println(10.0 + 3.14);	// 실수도 마찬가지
		
	}
}
